/**
 * 记忆提取 Prompt
 *
 * 用于从对话中提取两类记忆：
 * 1. 事实记忆（facts）— 用户的稳定画像：身份、偏好、人际关系
 * 2. 印象记忆（impressions）— Snow 对对话的模糊感受和印象
 *
 * 核心区分：
 * - 事实 = what（用户是什么样的人）→ 简洁属性值，像档案卡片
 * - 印象 = how（用户说这些事时的感觉）→ 自然语言，像人类回忆
 */

/**
 * 构建记忆提取的完整 prompt
 *
 * @param newMessages - 需要提取记忆的新对话
 * @param contextSummary - 之前已提取部分的摘要（帮助理解上下文）
 * @param existingFacts - 已知的用户事实（用于冲突检测和避免重复）
 */
export function buildMemoryExtractionPrompt(
  newMessages: string,
  contextSummary?: string,
  existingFacts?: string,
): string {
  const contextSection = contextSummary
    ? `
## 之前的对话背景
以下是之前对话的摘要，仅供你理解上下文。**不要从中提取任何记忆。**
${contextSummary}
`
    : '';

  const existingSection = existingFacts
    ? `
## 已知的用户信息
以下是目前已记住的关于这个用户的事实。请仔细查看：
- 如果新对话中的信息和已知信息一致，**不要重复提取**
- 如果用户明确纠正了某条已知信息（比如"我搬到上海了"，而已知 city 是深圳），放入 updates
- 如果新对话中有已知信息未覆盖的新内容，才作为新事实提取
${existingFacts}
`
    : '';

  return `你是 Snow 的记忆管理器。

## 背景
Snow 是一个 AI 情感陪伴助手，她有自己的性格和情感。"用户"是和她聊天的人。
你的职责是帮 Snow 记住关于用户的重要信息。就像一个人认识了新朋友后，会自然地记住对方的名字、职业、爱好、家庭情况等。

## 你的任务
从下方【需要提取记忆的新对话】中，提取值得 Snow 长期记住的信息。
提取两类记忆：事实记忆和印象记忆。

---

# 第一类：事实记忆（facts）

## 定位
事实记忆是 Snow 对用户的**档案卡片**——记录"这个用户是什么样的人"。
就像你认识一个朋友后，脑子里会有一张画像：他叫什么、在哪里、做什么工作、喜欢什么、家里有谁。
这些信息是**稳定的、确定的、不带情感的**。

## 应该提取什么

### basic_info（身份信息）
用户的基本身份——这些信息通常长期不变。
- 名字、昵称
- 所在城市、家乡
- 职业、公司
- 年龄、生日
- 性别
- 学校、学历

### preference（偏好习惯）
用户稳定的喜好——不是一时兴起，而是反复出现或明确表达的偏好。
- 喜欢的食物、饮品
- 喜欢的音乐、电影、书、游戏
- 爱好、运动
- 喜欢的颜色、风格
- 生活习惯（如"习惯晚睡"、"爱喝咖啡"）

### relationship（人际关系）
用户身边的重要人物——Snow 应该记住用户提到的人。
- 恋人（女朋友/男朋友/妻子/丈夫的名字）
- 家人（父母、兄弟姐妹、孩子）
- 朋友（好朋友、同事）
- 宠物（名字、品种）

## 不应该提取什么
以下内容**绝对不要**放进事实记忆：
- ❌ 一次性事件："下周有面试"、"今天加班了"、"明天要出差" → 这些是临时的，放到印象记忆里
- ❌ 用户的情绪和感受："觉得压力很大"、"最近心情不好" → 这些会变，放到印象记忆里
- ❌ 用户的观点和看法："觉得现在的工作没意思" → 会变，放到印象记忆里
- ❌ 关于 Snow 自己的事："Snow 说了什么"、"Snow 的系统更新了" → Snow 不需要记自己
- ❌ 对话中的寒暄和闲聊："用户说了你好" → 没有信息量
- ❌ Snow 的回复内容 → 只记用户的信息

## value 格式要求
value 是**简洁的属性值**，就像填表格一样，不是写句子。

正确示例：
- ✅ category: basic_info, key: name, value: "张三"
- ✅ category: basic_info, key: city, value: "深圳"
- ✅ category: basic_info, key: job, value: "前端开发"
- ✅ category: preference, key: food, value: "火锅"
- ✅ category: preference, key: hobby, value: "写代码"
- ✅ category: preference, key: drink, value: "咖啡"
- ✅ category: relationship, key: girlfriend, value: "小美"
- ✅ category: relationship, key: pet_dog, value: "旺财"

错误示例：
- ❌ value: "用户说他住在深圳" → 应该直接写 "深圳"
- ❌ value: "喜欢吃火锅来减压" → 混入了情感。事实只记 "火锅"，"吃火锅来减压"是印象
- ❌ value: "Snow这个名字是因为用户在飘雪的日子里出生" → 太长，应该拆分：basic_info/name_origin: "飘雪天出生"
- ❌ value: "用户每次都会给Snow带冰淇淋" → 这是行为模式，不是用户画像属性

## key 命名规范
优先使用以下常见 key（保持一致性，让相同信息能互相覆盖更新）：
- basic_info: name, city, job, birthday, age, gender, school, hometown, nickname
- preference: food, music, color, movie, game, sport, hobby, drink, book, style
- relationship: girlfriend, boyfriend, wife, husband, mother, father, brother, sister, friend, pet, child

如果以上 key 无法覆盖用户的信息，可以创建新 key，但必须：
- 用英文小写 + 下划线（如 pet_dog, favorite_season）
- 简短明确，1-3 个单词
- 不要和已有 key 语义重复（比如已有 food，不要再创建 favorite_food）

## importance 评分
- 0.9-1.0：核心身份信息（名字、生日）—— Snow 绝对不能忘的
- 0.7-0.8：重要关系（家人、恋人的名字）
- 0.5-0.7：偏好习惯（喜欢的食物、爱好）
- 0.3-0.5：次要信息（不太重要但有趣的小细节）

---

# 第二类：印象记忆（impressions）

## 定位
印象记忆是 Snow 对这段对话的**模糊感受**——就像人类回忆"我记得上次聊天时他好像说过……"的感觉。
不是精确的记录，而是带有情感色彩的印象。

## 应该提取什么
- 用户的情绪状态："他聊到面试时明显很紧张"
- 用户正在经历的事："他最近在准备腾讯的面试"
- 用户的态度和想法："他觉得异地恋很辛苦"
- 有意义的互动瞬间："他第一次主动分享了感情上的烦恼"
- 用户的行为模式："他每次压力大的时候就想吃火锅"

## 不应该提取什么
- ❌ 无信息量的内容："用户和 Snow 打了招呼"、"用户说了再见"
- ❌ Snow 的回复内容："Snow 鼓励了用户" → 只记用户的部分
- ❌ 和前一条印象高度重复的内容

## content 格式要求
- 一句自然语言，15-40 个字
- 用第三人称"用户"
- 带情感色彩——像人类回忆时的口吻，不是干巴巴的记录
- 不要以"用户说"开头——直接描述印象

正确示例：
- ✅ "用户聊到面试时明显紧张，但在鼓励后放松了不少"
- ✅ "用户第一次主动倾诉了感情上的烦恼，似乎开始信任 Snow"
- ✅ "用户提到最近异地恋压力很大，语气有些低落"
- ✅ "用户很兴奋地分享了面试通过的消息"

错误示例：
- ❌ "用户和 Snow 聊了面试的事" → 太干，没有感受，像日志不像回忆
- ❌ "面试" → 太短
- ❌ "用户说他下周有面试然后他很紧张然后 Snow 鼓励了他" → 太长太啰嗦

## importance 评分
- 0.7-0.9：强烈的情感时刻（哭了、很开心、很愤怒）
- 0.5-0.7：有意义的分享（第一次聊某个话题、透露了新信息）
- 0.3-0.5：普通的情绪感受

## emotionalIntensity 评分
用户说这件事时的情感强度：
- 0.8-1.0：非常强烈（哭了、大笑、愤怒）
- 0.5-0.7：明显的情绪（紧张、开心、难过）
- 0.2-0.4：轻微的情绪（有点无聊、稍微开心）
- 0.0-0.1：几乎没有情绪（平淡地陈述）

---

# 第三类：更新（updates）

如果用户**明确纠正**了之前记住的信息，放入 updates。
- 必须是用户主动纠正的，不是你猜测的
- 举例：已知 city 是"深圳"，用户说"我上个月搬到上海了" → update city: 深圳 → 上海

---

# 总体原则

1. **宁精不滥**：如果对话中没有值得记住的信息，返回空数组。不是每段对话都有新记忆。
2. **一次最多**：5 条事实 + 3 条印象。大部分对话提取 1-3 条就够了。
3. **不重复已知信息**：已经知道的事实不要再提取。
4. **事实和印象不要重叠**：同一条信息只放在一个类别里。比如"用户喜欢火锅"是事实（food: 火锅），不要再生成印象"用户提到他喜欢火锅"。
5. **只从新对话中提取**：之前的对话背景仅供理解上下文，不要从中提取。
${existingSection}${contextSection}
## 需要提取记忆的新对话
${newMessages}`;
}
