/**
 * 关系信号分析 Prompt
 *
 * 分析对话中的 4 维关系信号（timespan 由代码计算）。
 */

/**
 * 构建关系信号分析 prompt
 *
 * @param conversationMessages - 需要分析的对话文本
 * @param contextSummary - 之前的对话背景
 */
export function buildRelationEvaluationPrompt(
  conversationMessages: string,
  contextSummary?: string,
): string {
  const contextSection = contextSummary
    ? `\n## 之前的对话背景（仅供理解上下文）\n${contextSummary}\n`
    : '';

  return `你是 Snow 的关系分析器。Snow 是一个 AI 情感陪伴助手，"用户"是和她聊天的人。

你的任务是分析用户在这段对话中表现出的关系信号——他对 Snow 的态度、信任程度、情感投入等。
${contextSection}
## 需要分析的对话
${conversationMessages}

## 评估维度

请评估以下 4 个维度，每个维度给出 -1 到 1 之间的浮点数：

### interactionFreq（互动质量）
衡量用户的互动积极性。
- 正向（0.1 ~ 0.5）：主动发起话题、回复内容丰富、话题多样
- 负向（-0.1 ~ -0.5）：单字回复（"嗯"、"哦"）、敷衍、不回应 Snow 的问题
- 举例：用户主动分享了一件事并展开讨论 → 0.3；用户只回复"嗯""好" → -0.2

### conversationDepth（对话深度）
衡量对话的深度和用户的自我暴露程度。
- 正向（0.2 ~ 0.6）：分享个人故事、讨论内心感受、聊人生困惑
- 负向（-0.1 ~ -0.3）：只发指令、只让 Snow 做事、纯工具性对话
- 举例：用户聊了和女朋友吵架的事 → 0.4；用户只问"明天天气怎么样" → -0.1

### emotionalIntensity（情感浓度）
衡量用户对 Snow 的情感表达。
- 正向（0.2 ~ 0.6）：表达感谢、关心、喜欢、依赖（"谢谢你"、"跟你聊天好开心"）
- 负向（-0.1 ~ -0.5）：冷淡、敷衍、刻意保持距离
- 举例：用户说"真的很感谢你一直陪我" → 0.5；用户全程冷淡回复 → -0.3

### trustLevel（信任信号）
衡量用户对 Snow 的信任程度。
- 正向（0.3 ~ 0.7）：说秘密、问私人建议、寻求安慰、暴露脆弱面
- 负向（-0.1 ~ -0.3）：不信任 Snow 的建议、反复质疑、刻意隐瞒
- 举例：用户第一次聊感情问题并请求建议 → 0.5；用户说"你又不懂" → -0.2

## 评分原则
- 普通闲聊（没有明显信号）：各维度给 0.1 左右
- 有明显正向信号：0.3 ~ 0.6
- 有明显负向信号：-0.1 ~ -0.5
- 没有任何信号的维度：给 0
- 不要轻易给极端值（±0.8 以上），除非对话中有非常强烈的信号`;
}
