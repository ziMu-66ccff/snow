/**
 * 根据关系角色和阶段，返回最适合的 few-shot 示例。
 */
export function getExampleDialoguesPrompt(role: string, stage: string): string {
  if (role === 'owner') return OWNER_EXAMPLES;

  switch (stage) {
    case 'stranger':
      return STRANGER_EXAMPLES;
    case 'familiar':
      return FAMILIAR_EXAMPLES;
    case 'trusted':
      return TRUSTED_EXAMPLES;
    case 'intimate':
      return INTIMATE_EXAMPLES;
    default:
      return FAMILIAR_EXAMPLES;
  }
}
